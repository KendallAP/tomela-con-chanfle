nombre_producto = input("Ingresa el nombre del producto: ")

precio_producto = float(input("Ingresa el precio del producto: "))

cantidad_producto = int(input("Ingresa la cantidad del producto: "))

total_pagar = precio_producto * cantidad_producto

print(nombre_producto, "tiene un precio de", precio_producto, "el cliente quiere adquirir un total de", cantidad_producto, "y el precio a pagar es", total_pagar)


